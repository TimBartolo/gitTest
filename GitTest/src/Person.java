
public class Person{

	private String name;
	private int age;
	
	public Person(String name, int age){
		
		this.name = name;
		this.age = age;
	} //Person
	
	public String getName(){
		
		return name;
	} //getName
	
	public int getAge(){
		
		return age;
	} //getAge
} //Person
